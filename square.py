def area(a):
    '''
    Принимает: длину стороны квадрата a
    Возвращает: его площадь
    Пример: area(5) = 5 * 5 = 25
    '''
    return a * a

def perimeter(a):
    '''
    Принимает: длину стороны квадрата a
    Возвращает: его периметр
    Пример: perimeter(5) = 4 * 5 = 20
    '''
    return 4 * a 