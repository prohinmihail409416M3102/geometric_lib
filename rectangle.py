def area(a, b):
    '''
    Принимает: длины сторон прямоугольника a и b
    Возвращает: его площадь
    Пример: area(2, 3) = 2 * 3 = 6
    '''
    return a * b

def perimeter(a, b):
    '''
    Принимает: длины сторон прямоугольника a и b
    Возвращает: его периметр
    Пример: perimeter(2, 3) = (2 + 3) * 2 = 10
    '''
    return (a + b) * 2